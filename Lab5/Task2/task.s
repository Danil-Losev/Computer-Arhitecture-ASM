	AREA TASK, CODE, READONLY
    ENTRY

START
    MOV R1, #5
    MOV R0, #1
	BL FACTORIAL_LOOP
	B STOP

FACTORIAL_LOOP
    MOV R4, LR
	CMP R1, #1       
    BLE FACTORIAL_END
	MOV R2, R0
    MUL R0, R2, R1 
    SUB R1, R1, #1  
    B FACTORIAL_LOOP 
 
FACTORIAL_END
	MOV LR , R4
	BX LR

STOP B  STOP 
    END